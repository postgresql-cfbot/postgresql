INSERT INTO pgtbl_ddl_rt_orig
SELECT t.name, o.ord, o.line
FROM (VALUES
        ('basic'), ('id_cols'), ('id_custom'), ('gen_cols'), ('gen_virtual'), ('storage_cols'),
