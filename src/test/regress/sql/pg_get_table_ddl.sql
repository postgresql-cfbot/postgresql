ALTER TABLE ps_child ALTER COLUMN b SET STORAGE EXTERNAL;
SELECT stmt FROM pg_get_table_ddl('ps_parent', owner => false) stmt;

-- Issue 6: a foreign-table partition must produce a WARNING and be skipped
-- rather than causing an error.
CREATE FOREIGN DATA WRAPPER dummy_fdw;
