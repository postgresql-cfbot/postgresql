-- Issue 1: CLUSTER ON must come after the constraint that creates the index.
CREATE TABLE clu (a int, b int);
ALTER TABLE clu ADD CONSTRAINT clu_pk PRIMARY KEY (a);
CLUSTER clu USING clu_pk;
